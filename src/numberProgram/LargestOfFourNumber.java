//16) Java Program to Find Largest of Four Numbers.  

package numberProgram;
public class LargestOfFourNumber 
{
public static void main(String[] args) 
{
	int a=50;
	int b=100;
	int c=670;
	int d=90;
	
	if(a>b && a>c && a>d) 
	{System.out.println("Number A is greater");}
	
	else if (b>a && b>c && b>d) 
	{System.out.println("Number B is greater");}
	
	else if (c>a && c>b && c>d) 
	{System.out.println("Number C is greater");}
	
	else  
	{System.out.println("Number D is greater");}
		
}
}
