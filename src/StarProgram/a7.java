package StarProgram;

public class a7 
{
	public static void main (String [] args)
	{
	
		for(int i=1;i<=5;i++)
		{
			for(int j=4;j>=i;j--)
			{System.out.print(" ");}
			
			for(int m=1;m<=i;m++)
			{System.out.print("*");}
			
			System.out.println("");
		}
		
		for(int i=1;i<=4;i++)
		{
			for(int n=1;n<=i;n++)
			{System.out.print(" ");}
			
			for(int j=4;j>=i;j--)
			{System.out.print("*");}
			
			System.out.println("");
		}
		
		
		
	}
}
